export interface OutreachUserPromptArgs {
  name: string;
  category: string | null;
  fit_reason: string | null;
  context_notes?: string | null;
}

export function buildOutreachUserPrompt(args: OutreachUserPromptArgs): string {
  const ctx =
    args.context_notes && args.context_notes.trim()
      ? args.context_notes.trim()
      : "first contact";
  return `Draft an outreach email to ${args.name} (${args.category ?? "uncategorized"}).
Fit reason: ${args.fit_reason ?? "not provided"}
Context: ${ctx}

Output strict JSON: {subject: string under 50 chars, body: string under 200 words}`;
}

// Any [...] up to 100 chars, no newlines inside. For plaintext outreach
// every such pattern is a placeholder token ("[contact info]", "[your email]").
// Markdown link syntax would be collateral, but we don't ask for markdown.
const PLACEHOLDER_PATTERN = /\[[^\]\n]{1,100}\]/g;

export function stripPlaceholders(text: string): string {
  let result = text.replace(PLACEHOLDER_PATTERN, "");
  result = result.split("\n").map((l) => l.replace(/[ \t]+$/, "")).join("\n");
  result = result.replace(/\n{3,}/g, "\n\n");
  return result.trim();
}

const escapeRegex = (s: string): string => s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");

export function findBannedWords(text: string, banned: string[]): string[] {
  const found: string[] = [];
  const lower = text.toLowerCase();
  for (const word of banned) {
    const w = word.trim();
    if (!w) continue;
    let hit = false;
    if (w.includes(" ")) {
      // Multi-word phrases: case-insensitive substring is correct
      hit = lower.includes(w.toLowerCase());
    } else {
      // Single words: word-boundary regex avoids false positives
      hit = new RegExp(`\\b${escapeRegex(w)}\\b`, "i").test(text);
    }
    if (hit && !found.includes(word)) found.push(word);
  }
  return found;
}
