import type { Bindings } from "../lib/types";

export interface VoiceReference {
  rules: {
    voice_is: string[];
    voice_is_not: string[];
    banned_words: string[];
    cast?: unknown;
  };
  samples: {
    email_to_member?: string[];
    trip_notes?: string[];
    cold_open_vo?: string[];
    build_vo?: string[];
    meaning_bridge_vo?: string[];
    [k: string]: string[] | undefined;
  };
  outreach_calibration_note?: string;
  version?: string;
  updated?: string;
}

export async function loadVoiceReference(env: Bindings): Promise<VoiceReference> {
  const raw = await env.SECRETS.get("voice_reference");
  if (!raw) {
    throw new Error("voice_reference not set in KV namespace SECRETS");
  }
  let parsed: unknown;
  try {
    parsed = JSON.parse(raw);
  } catch (e) {
    throw new Error(
      "voice_reference KV value is not valid JSON: " +
        (e instanceof Error ? e.message : String(e)),
    );
  }
  if (typeof parsed !== "object" || parsed === null) {
    throw new Error("voice_reference must be a JSON object");
  }
  const v = parsed as Partial<VoiceReference>;
  if (
    !v.rules ||
    !Array.isArray(v.rules.voice_is) ||
    !Array.isArray(v.rules.voice_is_not) ||
    !Array.isArray(v.rules.banned_words)
  ) {
    throw new Error(
      "voice_reference.rules must contain voice_is[], voice_is_not[], banned_words[]",
    );
  }
  if (!v.samples || typeof v.samples !== "object") {
    throw new Error("voice_reference.samples is required");
  }
  return v as VoiceReference;
}

const formatCast = (cast: unknown): string => {
  if (typeof cast === "string") return cast;
  if (Array.isArray(cast)) return cast.join("; ");
  if (cast && typeof cast === "object") {
    return Object.entries(cast as Record<string, unknown>)
      .map(([k, val]) => `${k}: ${typeof val === "string" ? val : JSON.stringify(val)}`)
      .join("; ");
  }
  return "(unspecified)";
};

const formatSamples = (items: string[] | undefined, limit?: number): string => {
  if (!items || items.length === 0) return "(none provided)";
  const slice = limit ? items.slice(0, limit) : items;
  return slice.map((s, i) => `--- sample ${i + 1} ---\n${s}`).join("\n\n");
};

